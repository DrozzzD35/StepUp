package old;

public class IfElse {
    public static void main(String[] args) {
        int myInt = 15;
        if (myInt <= 10) {
            System.out.println("true");
        } else if (myInt > 20) {
            System.out.println("false");
        } else{
            System.out.println("not true, not false");
        }
    }
}


